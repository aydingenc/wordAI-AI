// Generates the ignored supabase/seed_canonical_queue.sql file: batched INSERT
// statements that fill private.canonical_content_build_queue_staging, followed
// by a call to public.load_canonical_content_build_queue_service(7368).
//
// Usage:
//   node scripts/build-canonical-queue-seed.mjs --queue <path> [--output <path>]

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import process from 'node:process';
import { parseCsvLine } from './phase2a-catalog.mjs';

const QUEUE_COLUMNS = [
  'queue_type', 'queue_order', 'canonical_id', 'canonical_word',
  'best_subtlex_rank', 'aggregate_corpus_count', 'source_forms', 'lemma_notes',
  'initial_status', 'lexical_validation', 'db_recheck', 'content_generation', 'cost_approval',
];
const STAGING_COLUMNS = [
  'queue_type', 'queue_order', 'canonical_id', 'canonical_word',
  'best_subtlex_rank', 'aggregate_corpus_count', 'source_forms', 'lemma_notes',
];

function sqlLiteral(value) {
  if (value === null || value === undefined || value === '') return 'null';
  return `'${String(value).replaceAll("'", "''")}'`;
}
function sqlIntLiteral(value) {
  if (value === null || value === undefined || value === '') return 'null';
  return String(parseInt(value, 10));
}

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    if (argv[i].startsWith('--')) { args[argv[i].slice(2)] = argv[i + 1]; i += 1; }
  }
  return args;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (!args.queue) {
    console.error('Usage: node scripts/build-canonical-queue-seed.mjs --queue <path> [--output <path>]');
    process.exit(2);
  }
  const outputPath = resolve(args.output ?? 'supabase/seed_canonical_queue.sql');

  const source = await readFile(resolve(args.queue), 'utf8');
  const lines = source.replace(/\r\n?/g, '\n').split('\n');
  if (lines.at(-1) === '') lines.pop();
  const header = parseCsvLine(lines[0].replace(/^﻿/, ''), 1);
  QUEUE_COLUMNS.forEach((column, index) => {
    if (header[index] !== column) throw new Error(`header column ${index + 1} must be ${column}, got ${header[index]}`);
  });
  const rows = lines.slice(1).map((line, index) => {
    const values = parseCsvLine(line, index + 2);
    return Object.fromEntries(header.map((column, columnIndex) => [column, values[columnIndex]]));
  });

  const targetRows = rows.filter((r) => r.queue_type === 'TARGET');
  const reserveRows = rows.filter((r) => r.queue_type === 'RESERVE');
  if (targetRows.length !== 6868) throw new Error(`expected 6868 TARGET rows, got ${targetRows.length}`);
  if (reserveRows.length !== 500) throw new Error(`expected 500 RESERVE rows, got ${reserveRows.length}`);
  const distinctIds = new Set(rows.map((r) => r.canonical_id));
  if (distinctIds.size !== rows.length) throw new Error('duplicate canonical_id in queue CSV');

  const batches = [];
  for (let offset = 0; offset < rows.length; offset += 100) {
    const chunk = rows.slice(offset, offset + 100);
    const values = chunk.map((row) => `  (${[
      sqlLiteral(row.queue_type),
      sqlIntLiteral(row.queue_order),
      sqlLiteral(row.canonical_id),
      sqlLiteral(row.canonical_word),
      sqlIntLiteral(row.best_subtlex_rank),
      sqlIntLiteral(row.aggregate_corpus_count),
      sqlLiteral(row.source_forms),
      sqlLiteral(row.lemma_notes),
    ].join(', ')})`).join(',\n');
    batches.push(`insert into private.canonical_content_build_queue_staging (${STAGING_COLUMNS.join(', ')}) values\n${values};`);
  }

  const sql = [
    '-- GENERATED by scripts/build-canonical-queue-seed.mjs. Do not edit or commit.',
    `-- target=${targetRows.length} reserve=${reserveRows.length}`,
    '',
    'begin;',
    'set local statement_timeout = 0;',
    'set local standard_conforming_strings = on;',
    'truncate table private.canonical_content_build_queue_staging;',
    ...batches,
    'select public.load_canonical_content_build_queue_service(7368);',
    'commit;',
    '',
  ].join('\n');

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, sql, { encoding: 'utf8', mode: 0o600 });

  console.log('Canonical queue seed generated:', {
    target: targetRows.length,
    reserve: reserveRows.length,
    output: outputPath,
    outputBytes: Buffer.byteLength(sql),
  });
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
